#definir variable

nombre = "Luis"

#concatenar con + (no es tan usado)

bienvenida = "Hola " + nombre + " como estás?"

print(bienvenida)

#concatenar con f-strings

bienvenida = f"Hola {nombre} ¿Cómo estás?"

print(bienvenida)

#operadores de pertenencia (in / not in)

print("Lucas" in bienvenida) #true

print("Lucas" not in bienvenida) #false


