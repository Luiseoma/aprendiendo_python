numeros = [5, 8, 1, 10, 4]

numero_alto = max(numeros)

numero_bajo = min(numeros)

suma_total = sum(numeros)

cantidad = len(numeros)

print(numero_alto)
print(numero_bajo)
print(suma_total)
print(cantidad)
